
<h1>FAQ</h1>

<h3>What does my user Level mean?</h3>

<ul>
    <li><label>lvl 0</label><span>Not registerd. you can only look but not touch</span></li>
    <li><label>lvl 1</label><span>you can log in and can edit most stuff</span></li>
    <li><label>lvl 2</label><span>you can edit complex shows where the mapping is not easy to understand</span></li>
    <li><label>lvl 3</label><span>you can revert to revisions, mass edit / delete a show</span></li>
    <li><label>lvl 4</label><span>you can set the level of other users</span></li>
    <li><label>lvl 5</label><span>you are an admin you may kick other users with same or lower lvl</span></li>
</ul>


<h3>What does the lvl of a show mean?</h3>
<p>
This means "lock level". You will need at least this level to edit the show. Most shows will have a lvl 1 or 2
</p>

<h3>What are 'passthrus'?</h3>
<p>
Passthrus are a way to simplify the connection between two entities.
</p>

<ul>
    <li><label><span class="absolute shadow">Absolute</span></label>The two entities have there episodes linked by the absolute numbers. Season and Episode are calculated individualy.</li>
    <li><label><span class="sxxexx shadow">SxxExx</span></label>The two entities have there episodes linked by the season-episode numbers. Absolute numbers are calculated individualy.</li>
    <li><label><span class="full shadow">Full</span></label>The two entities share the season, episode and absolute numbers.</li>
</ul>

