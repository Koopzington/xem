
<h1>FAQ</h1>

<h3>What does my user Level mean?</h3>
<p>
    <ul>
        <li><label>lvl 0</label><span>Not registerd. you can only look but not touch</span></li>
        <li><label>lvl 1</label><span>you can log in and can edit most stuff</span></li>
        <li><label>lvl 2</label><span>you can edit complex shows where the mapping is not easy to understand</span></li>
        <li><label>lvl 3</label><span>you can revert to revisions, mass edit / delete a show</span></li>
        <li><label>lvl 4</label><span>you can set the level of other users</span></li>
        <li><label>lvl 5</label><span>you are an admin you may kick other users with same or lower lvl</span></li>
    </ul>
</p>


